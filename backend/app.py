# ─────────────────────────────────────────────────────────────
# Helper: build lifestyle and medical prescriptions
# ─────────────────────────────────────────────────────────────
def _build_prescriptions(data, diabetic):
    """Return specific dietary, physical, and clinical prescriptions."""
    try:
        glucose = float(data.get("Glucose", 100))
        bmi     = float(data.get("BMI", 22))
        bp      = float(data.get("BloodPressure", 70))
    except (ValueError, TypeError):
        glucose = 100.0
        bmi = 22.0
        bp = 70.0

    # 1. Diet Prescription
    if glucose > 125:
        diet = "Strict low-glycemic index (GI) diet. Limit simple sugars and carbohydrates. Increase dietary fibre."
    elif glucose > 99:
        diet = "Moderate carbohydrate restriction. Focus on complex carbs, whole grains, and lean proteins."
    else:
        diet = "Maintain balanced macro distribution. Focus on clean proteins, healthy fats, and high-fibre vegetables."

    # 2. Exercise Prescription
    if bmi >= 30:
        exercise = "Cardio-respiratory training: 30-45 mins of low-impact aerobic exercise (walking, swimming) 5 days/week."
    elif bmi >= 25:
        exercise = "Moderate physical activity: 150 mins/week of brisk walking or cycling, combined with light resistance training."
    else:
        exercise = "Maintain active lifestyle: 30 mins of moderate physical activity daily, plus strength training 2 days/week."

    # 3. Clinical Follow-up Prescription
    if diabetic:
        clinical = "Urgent: Consult a primary care physician or endocrinologist. Request an HbA1c screening test."
    elif glucose > 99 or bp > 80:
        clinical = "Routine check-up: Monitor blood glucose and blood pressure quarterly. Consult your doctor."
    else:
        clinical = "Preventative: Annual wellness screening and routine biometric checks recommended."

    return {
        "diet": diet,
        "exercise": exercise,
        "clinical": clinical
    }


# ─────────────────────────────────────────────────────────────
# Helper: build personalised recommendations
# ─────────────────────────────────────────────────────────────
def _build_recommendations(data, diabetic):
    """Return 3 personalised health tips based on input values."""
    tips = []

    try:
        glucose = float(data.get("Glucose", 100))
        bmi     = float(data.get("BMI", 22))
        bp      = float(data.get("BloodPressure", 70))
        age     = float(data.get("Age", 30))
        insulin = float(data.get("Insulin", 80))
    except (ValueError, TypeError):
        glucose = 100.0
        bmi = 22.0
        bp = 70.0
        age = 30.0
        insulin = 80.0

    if glucose > 99:
        tips.append("Reduce sugar and refined-carbohydrate intake to lower your glucose levels.")
    if bmi >= 25:
        tips.append("Aim for a BMI of 18.5–24.9 through balanced diet and regular exercise.")
    if bp > 80:
        tips.append("Monitor blood pressure regularly and reduce sodium intake.")
    if insulin > 166:
        tips.append("Discuss insulin sensitivity strategies with your doctor.")
    if age >= 45:
        tips.append("Adults over 45 should have annual glucose screening.")

    # Always include these positives
    general = [
        "Exercise for at least 30 minutes of moderate activity most days.",
        "Maintain a healthy weight and balanced nutrition.",
        "Stay hydrated and limit processed foods.",
        "Schedule regular health check-ups with your doctor.",
        "Get 7–9 hours of quality sleep each night.",
    ]
    for g in general:
        if len(tips) >= 3:
            break
        tips.append(g)

    return tips[:3]


