# Training medians used to impute missing (0.0) values at inference time
MEDIANS = {
    "Glucose": 112.0,
    "BloodPressure": 72.0,
    "SkinThickness": 31.0,
    "Insulin": 115.0,
    "BMI": 31.6,
}

