@app.route("/api/predict", methods=["POST"])
def predict():
    if model is None or scaler is None:
        return jsonify({"error": "Model not loaded. Run model_training.py first."}), 503

    data = request.get_json(force=True)

    # Validate required fields
    missing = [f for f in FEATURE_NAMES if f not in data]
    if missing:
        return jsonify({"error": f"Missing fields: {missing}"}), 400

    try:
        # Impute zero values (representing missing data) with training medians before predicting
        input_dict = {f: float(data[f]) for f in FEATURE_NAMES}
        for col, median_val in MEDIANS.items():
            if input_dict[col] == 0.0:
                input_dict[col] = median_val

        input_values = [input_dict[f] for f in FEATURE_NAMES]
    except (ValueError, TypeError) as e:
        return jsonify({"error": f"Invalid value: {e}"}), 400

    input_df = pd.DataFrame([input_values], columns=FEATURE_NAMES)
    scaled   = scaler.transform(input_df)

    prediction  = int(model.predict(scaled)[0])
    probability = float(model.predict_proba(scaled)[0][1])

    risk_level = (
        "High Risk"     if probability > 0.7 else
        "Moderate Risk" if probability > 0.4 else
        "Low Risk"
    )

    diabetic = prediction == 1
    recommendations = _build_recommendations(data, diabetic)
    prescriptions = _build_prescriptions(data, diabetic)

    message = (
        "Your metrics indicate elevated diabetes risk. Please consult a healthcare professional promptly."
        if diabetic else
        "Your health metrics suggest a low probability of diabetes. Keep maintaining your healthy lifestyle!"
    )

    return jsonify({
        "prediction":       prediction,
        "probability":      round(probability * 100, 1),
        "risk_level":       risk_level,
        "diabetic":         diabetic,
        "message":          message,
        "recommendations":  recommendations,
        "prescriptions":    prescriptions,
        "input_summary": {f: data[f] for f in FEATURE_NAMES},
    })


